
public class Counter
{
    int initialValue = 0;
    
    void increaseByOne() {
        initialValue += 1;
    }
    
    void decreaseByOne() {
        initialValue -= 1;
    }
    
    void increaseByTen() {
        initialValue += 10;
    }
    
    void decreaseByTen() {
        initialValue -= 10;
    }
}
